  function getCount(str) {
  var vowelsCount = 0;
  
  
   let splitSt = str.split('');
   for(let i = 0; i < splitSt.length; i++){
   if(splitST[i] == a ||  splitST[i] == e || splitST[i] == i || splitST[i] == o || splitST[i] == u){
   vowelsCount++


   }
   } 
  return vowelsCount;
}


















